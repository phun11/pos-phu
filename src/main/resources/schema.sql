-- =========================================================================
-- PHẦN 5: BỘ TRIGGER ĐIỀU HƯỚNG TỰ ĐỘNG (AUTOPILOT)
-- =========================================================================

-- =========================================================================
-- [SIÊU TRIGGER 1]: TRUNG TÂM ĐIỀU KHIỂN KHO TỪ LICH_SU_KHO (Ý TƯỞNG CỦA BẠN)
-- Mọi hành động Insert vào bảng này sẽ tự động cập nhật SANPHAM
-- =========================================================================
CREATE OR REPLACE TRIGGER TRG_LICH_SU_KHO_BEFORE_INSERT
BEFORE INSERT ON LICH_SU_KHO
FOR EACH ROW
DECLARE
    v_tonkho_hientai NUMBER := 0;
    v_vaitro VARCHAR2(20);
    v_sanpham_id_moi NUMBER;
    v_is_new_product BOOLEAN := FALSE; -- Biến cờ đánh dấu sản phẩm mới tạo
BEGIN
    -- 1. KIỂM TRA VÀ XỬ LÝ SẢN PHẨM (NẾU KHÔNG CÓ ID HOẶC ID KHÔNG TỒN TẠI)
    IF :NEW.sanpham_id IS NULL THEN
        -- Trường hợp 1: Không truyền ID -> Tạo mới sản phẩm
        -- Nếu là phiếu NHẬP, ta cho tồn kho ban đầu bằng chính số lượng nhập, ngược lại bằng 0
        INSERT INTO SANPHAM (ten, category, giaban, tonkho, image_url)
        VALUES ('Sản phẩm mới tự động tạo', 'Chưa phân loại', 0, 
                CASE WHEN UPPER(TRIM(:NEW.loai_phieu)) LIKE 'NHAP%' THEN :NEW.so_luong ELSE 0 END, 
                NULL)
        RETURNING id INTO v_sanpham_id_moi;

        :NEW.sanpham_id := v_sanpham_id_moi;
        v_tonkho_hientai := 0;
        v_is_new_product := TRUE; -- Đánh dấu đã xử lý xong kho cho sản phẩm mới này
    ELSE
        -- Trường hợp 2: Có truyền ID, kiểm tra sự tồn tại
        BEGIN
            SELECT tonkho INTO v_tonkho_hientai 
            FROM SANPHAM 
            WHERE id = :NEW.sanpham_id 
            FOR UPDATE; -- Khóa dòng chống Loss Update
        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                -- Nếu ID không tồn tại -> Tạo mới sản phẩm với ID đó
                INSERT INTO SANPHAM (id, ten, category, giaban, tonkho, image_url)
                VALUES (:NEW.sanpham_id, 'Sản phẩm lỗi ID - Tự động tạo', 'Hệ thống', 0, 
                        CASE WHEN UPPER(TRIM(:NEW.loai_phieu)) LIKE 'NHAP%' THEN :NEW.so_luong ELSE 0 END, 
                        NULL);
                
                v_tonkho_hientai := 0;
                v_is_new_product := TRUE; -- Đánh dấu đã xử lý xong kho
        END;
    END IF;

    -- 2. KIỂM TRA VAI TRÒ NGƯỜI THỰC HIỆN
    IF :NEW.nguoi_thuc_hien IS NOT NULL THEN
        BEGIN
            SELECT vaitro INTO v_vaitro FROM NHANVIEN WHERE id = :NEW.nguoi_thuc_hien;
        EXCEPTION
            WHEN NO_DATA_FOUND THEN v_vaitro := 'NHANVIEN';
        END;
    ELSE
        v_vaitro := 'NHANVIEN';
    END IF;

    -- 3. LOGIC TỰ ĐỘNG GÁN GHI CHÚ (Khi để trống)
    IF :NEW.loai_phieu IS NULL AND :NEW.ghi_chu IS NULL THEN
        IF v_vaitro = 'ADMIN' THEN
            :NEW.loai_phieu := 'NHAP_KHO';
            :NEW.ghi_chu := 'Nhập kho tự động';
        ELSE
            :NEW.loai_phieu := 'XUAT_BAN';
            :NEW.ghi_chu := 'Bán HĐ tự động';
        END IF;
    END IF;

    -- 4. THỰC THI CẬP NHẬT KHO THỰC TẾ (Chỉ chạy khi KHÔNG PHẢI sản phẩm vừa tạo mới)
    IF UPPER(TRIM(:NEW.loai_phieu)) LIKE 'XUAT%' THEN
        -- Logic Xuất Kho
        IF v_tonkho_hientai - :NEW.so_luong < 0 THEN
            RAISE_APPLICATION_ERROR(-20002, 'Sản phẩm ID ' || :NEW.sanpham_id || ' không đủ tồn kho để xuất!');
        END IF;

        :NEW.tonkho_sau := v_tonkho_hientai - :NEW.so_luong;
        UPDATE SANPHAM SET tonkho = :NEW.tonkho_sau WHERE id = :NEW.sanpham_id;

    ELSe
        -- Logic Nhập Kho
        IF v_is_new_product THEN
            -- Nếu là sản phẩm vừa được tạo ở Bước 1, tồn kho sau chính là số lượng nhập
            :NEW.tonkho_sau := :NEW.so_luong;
        ELSE
            -- Nếu là sản phẩm cũ đã có sẵn, thực hiện cộng dồn bình thường
            :NEW.tonkho_sau := v_tonkho_hientai + :NEW.so_luong;
            UPDATE SANPHAM SET tonkho = :NEW.tonkho_sau WHERE id = :NEW.sanpham_id;
        END IF;
    END IF;
END;
/

-- TRIGGER 2: Tự động gán tiền mặt ban đầu theo định danh của máy POS
CREATE OR REPLACE TRIGGER TRG_CALAM_TIENMAT_BANDAU
BEFORE INSERT ON CALAM
FOR EACH ROW
DECLARE
    v_tien_ketca_truoc NUMBER := 0;
BEGIN
    IF :NEW.tienmat_bandau = 0 OR :NEW.tienmat_bandau IS NULL THEN
        BEGIN
            SELECT tienmat_ketca INTO v_tien_ketca_truoc FROM CALAM
            WHERE may_pos = NVL(:NEW.may_pos, 1) AND ketthuc IS NOT NULL
            ORDER BY id DESC FETCH FIRST 1 ROWS ONLY;
        EXCEPTION
            WHEN NO_DATA_FOUND THEN v_tien_ketca_truoc := 0;
        END;
        :NEW.tienmat_bandau := NVL(v_tien_ketca_truoc, 0);
    END IF;
END;
/

-- TRIGGER 3: Tự động móc Hóa Đơn vào Ca Làm 
CREATE OR REPLACE TRIGGER TRG_HOADON_BEFORE_INSERT
BEFORE INSERT ON HOADON
FOR EACH ROW
BEGIN
    IF :NEW.calam_id IS NULL THEN
        BEGIN
            SELECT id INTO :NEW.calam_id FROM CALAM
            WHERE nhanvien_id = :NEW.nhanvien_id AND ketthuc IS NULL
            ORDER BY id DESC FETCH FIRST 1 ROWS ONLY;
        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                RAISE_APPLICATION_ERROR(-20001, 'Nhân viên chưa mở ca làm việc! Không thể tạo hóa đơn.');
        END;
    END IF;
END;
/

-- TRIGGER 4: Tích điểm & Cộng tiền ngay khi Hóa Đơn Mới thành công
CREATE OR REPLACE TRIGGER TRG_HOADON_AFTER_INSERT
AFTER INSERT ON HOADON
FOR EACH ROW
BEGIN
    IF :NEW.trangthai = 'HOAN_THANH' THEN
        -- Tích điểm 
        IF :NEW.khachhang_id IS NOT NULL THEN
            UPDATE KHACHHANG SET diemtichluy = diemtichluy + FLOOR(:NEW.tongtien / 10000) WHERE id = :NEW.khachhang_id;
        END IF;

        -- Cộng tiền vào Ca Làm 
        IF UPPER(:NEW.phuongthuc_tt) LIKE '%TIỀN MẶT%' OR UPPER(:NEW.phuongthuc_tt) LIKE '%TIEN MAT%' THEN
            UPDATE CALAM SET tong_doanhthu = NVL(tong_doanhthu, 0) + :NEW.tongtien, tien_mat = NVL(tien_mat, 0) + :NEW.tongtien WHERE id = :NEW.calam_id;
        ELSE
            UPDATE CALAM SET tien_nganhang = NVL(tien_nganhang, 0) + :NEW.tongtien, tong_doanhthu = NVL(tong_doanhthu, 0) + :NEW.tongtien WHERE id = :NEW.calam_id;
        END IF;
    END IF;
END;
/

-- TRIGGER 5: Đẩy lệnh trừ kho vào LICH_SU_KHO khi chèn CTHD
-- (Lúc này TRIGGER này cực kỳ nhẹ, chỉ việc thảy lệnh cho LICH_SU_KHO xử lý)
CREATE OR REPLACE TRIGGER TRG_CTHD_AFTER_INSERT
AFTER INSERT ON CTHD
FOR EACH ROW
DECLARE
    v_trangthai VARCHAR2(20);
    v_nhanvien_id NUMBER;
BEGIN
    SELECT trangthai, nhanvien_id INTO v_trangthai, v_nhanvien_id FROM HOADON WHERE id = :NEW.hoadon_id;

    IF v_trangthai IN ('HOAN_THANH', 'CHO_THANH_TOAN') THEN
        -- Chỉ cần Insert, Trigger của LICH_SU_KHO sẽ tự động lo việc Trừ SANPHAM
        INSERT INTO LICH_SU_KHO (sanpham_id, loai_phieu, so_luong, nguoi_thuc_hien, ghi_chu)
        VALUES (:NEW.sanpham_id, 'XUAT_BAN', :NEW.soluong, v_nhanvien_id, 'Bán HĐ: ' || :NEW.hoadon_id);
    END IF;
END;
/

-- TRIGGER 6: Xử lý VNPay & Hủy đơn
CREATE OR REPLACE TRIGGER TRG_HOADON_AFTER_UPDATE
AFTER UPDATE OF trangthai ON HOADON
FOR EACH ROW
BEGIN
    -- KHỐI 1A: Hủy Đơn (Đẩy lệnh cho LICH_SU_KHO tự động cộng lại kho)
    IF :OLD.trangthai IN ('HOAN_THANH', 'CHO_THANH_TOAN') AND :NEW.trangthai = 'THAT_BAI' THEN
        FOR r IN (SELECT sanpham_id, soluong FROM CTHD WHERE hoadon_id = :NEW.id) LOOP
            INSERT INTO LICH_SU_KHO (sanpham_id, loai_phieu, so_luong, nguoi_thuc_hien, ghi_chu)
            VALUES (r.sanpham_id, 'NHAP_HUY_DON', r.soluong, :NEW.nhanvien_id, 'Hủy HĐ: ' || :NEW.id);
        END LOOP;
    END IF;

    -- KHỐI 1B: Khôi phục đơn bị bấm nhầm
    IF :OLD.trangthai = 'THAT_BAI' AND :NEW.trangthai IN ('HOAN_THANH', 'CHO_THANH_TOAN') THEN
        FOR r IN (SELECT sanpham_id, soluong FROM CTHD WHERE hoadon_id = :NEW.id) LOOP
            INSERT INTO LICH_SU_KHO (sanpham_id, loai_phieu, so_luong, nguoi_thuc_hien, ghi_chu)
            VALUES (r.sanpham_id, 'XUAT_BAN', r.soluong, :NEW.nhanvien_id, 'Khôi phục HĐ: ' || :NEW.id);
        END LOOP;
    END IF;

    -- KHỐI 2A: Chốt tiền & Điểm (Chỉ tính khi Thành Công)
    IF :OLD.trangthai <> 'HOAN_THANH' AND :NEW.trangthai = 'HOAN_THANH' THEN
        IF :NEW.khachhang_id IS NOT NULL THEN
            UPDATE KHACHHANG SET diemtichluy = diemtichluy + FLOOR(:NEW.tongtien / 10000) WHERE id = :NEW.khachhang_id;
        END IF;

        IF UPPER(:NEW.phuongthuc_tt) LIKE '%TIỀN MẶT%' OR UPPER(:NEW.phuongthuc_tt) LIKE '%TIEN MAT%' THEN
            UPDATE CALAM SET tong_doanhthu = NVL(tong_doanhthu, 0) + :NEW.tongtien, tien_mat = NVL(tien_mat, 0) + :NEW.tongtien WHERE id = :NEW.calam_id;
        ELSE
            UPDATE CALAM SET tien_nganhang = NVL(tien_nganhang, 0) + :NEW.tongtien, tong_doanhthu = NVL(tong_doanhthu, 0) + :NEW.tongtien WHERE id = :NEW.calam_id;
        END IF;
    END IF;

    -- KHỐI 2B: Trừ tiền & Điểm (Nếu Hủy đơn đã chốt)
    IF :OLD.trangthai = 'HOAN_THANH' AND :NEW.trangthai <> 'HOAN_THANH' THEN
        IF :OLD.khachhang_id IS NOT NULL THEN
            UPDATE KHACHHANG SET diemtichluy = GREATEST(0, diemtichluy - FLOOR(:OLD.tongtien / 10000)) WHERE id = :OLD.khachhang_id; 
        END IF;

        IF UPPER(:OLD.phuongthuc_tt) LIKE '%TIỀN MẶT%' OR UPPER(:OLD.phuongthuc_tt) LIKE '%TIEN MAT%' THEN
            UPDATE CALAM SET tong_doanhthu = GREATEST(0, NVL(tong_doanhthu, 0) - :OLD.tongtien), tien_mat = GREATEST(0, NVL(tien_mat, 0) - :OLD.tongtien) WHERE id = :OLD.calam_id;
        ELSE
            UPDATE CALAM SET tien_nganhang = GREATEST(0, NVL(tien_nganhang, 0) - :OLD.tongtien), tong_doanhthu = GREATEST(0, NVL(tong_doanhthu, 0) - :OLD.tongtien) WHERE id = :OLD.calam_id;
        END IF;
    END IF;
END;
/

-- TRIGGER 7: Tạo mã Nhân Viên
CREATE OR REPLACE TRIGGER TRG_NHANVIEN_MA
BEFORE INSERT ON NHANVIEN
FOR EACH ROW
DECLARE
    -- Sử dụng giao dịch tự trị để tránh lỗi Mutating Table (ORA-04091)
    PRAGMA AUTONOMOUS_TRANSACTION;
    v_yymm VARCHAR2(4);
    v_max_stt NUMBER;
BEGIN
    -- Lấy tháng hiện tại định dạng YYMM (VD: 2605)
    v_yymm := TO_CHAR(SYSDATE, 'YYMM');
    
    -- Tìm số thứ tự lớn nhất của tháng hiện tại, nếu chưa có ai thì gán 0
    SELECT NVL(MAX(TO_NUMBER(SUBSTR(ma_nhanvien, 5, 2))), 0)
    INTO v_max_stt
    FROM NHANVIEN
    WHERE ma_nhanvien LIKE v_yymm || '%';
    
    -- Ghép YYMM với số thứ tự tiếp theo (LPAD để đảm bảo 2 chữ số 01, 02...)
    :NEW.ma_nhanvien := v_yymm || LPAD(v_max_stt + 1, 2, '0');
    
    -- Bắt buộc phải có COMMIT khi dùng AUTONOMOUS_TRANSACTION
    COMMIT; 
END;

